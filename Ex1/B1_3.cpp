// Nhập vào 2 số nguyên bất kỳ :
// Tính tổng , thương, tích, hiệu của 2 số.
#include<iostream>
using namespace std ;
int main()
{
    int a , b ;
    cout << "Nhap a = ";
    cin >> a ;
    cout << "Nhap b = ";
    cin >> b ;
    cout << a << " + " << b << " = " << a + b <<endl ;
    cout << a << " - " << b << " = " << a - b <<endl ;
    cout << a << " * " << b << " = " << a * b <<endl ;
    cout << a << " / " << b << " = " << (float) a / b <<endl ;
}
